void indigocat_jupiter_equatorial_coords(double JD, equatorial_coords_s * position) {
	heliocentric_coords_s h_sol, h_jupiter;
	cartesian_coords_s g_sol, g_jupiter;
	double a,b,c;
	double ra, dec, delta, diff, last, t = 0;

	indigocat_sun_geometric_coords(JD, &h_sol);
	indigocat_heliocentric_to_cartesian_coords(&h_sol,  &g_sol);

	do {
		last = t;
		indigocat_jupiter_heliocentric_coords(JD - t, &h_jupiter);
		indigocat_heliocentric_to_cartesian_coords(&h_jupiter, &g_jupiter);

		a = g_sol.X + g_jupiter.X;
		b = g_sol.Y + g_jupiter.Y;
		c = g_sol.Z + g_jupiter.Z;

		delta = a*a + b*b + c*c;
		delta = sqrt (delta);
		t = delta * 0.0057755183;
		diff = t - last;
	} while (diff > 0.0001 || diff < -0.0001);

	ra = atan2 (b,a);
	dec = c / delta;
	dec = asin (dec);

	position->ra = indigocat_range_degrees(RAD2DEG * ra);
	position->dec = RAD2DEG * dec;
}

void indigocat_jupiter_heliocentric_coords (double JD, heliocentric_coords_s * position) {
	double t, t2, t3, t4, t5;
	double L0, L1, L2, L3, L4, L5;
	double B0, B1, B2, B3, B4, B5;
	double R0, R1, R2, R3, R4, R5;

	/* get julian ephemeris day */
	t = (JD - 2451545.0) / 365250;
	t2 = t * t;
	t3 = t2 * t;
	t4 = t3 * t;
	t5 = t4 * t;

	/* calc L series */
	L0 = indigocat_vsop87_calc_series(jupiter_longitude_l0, LONG_L0, t);
	L1 = indigocat_vsop87_calc_series(jupiter_longitude_l1, LONG_L1, t);
	L2 = indigocat_vsop87_calc_series(jupiter_longitude_l2, LONG_L2, t);
	L3 = indigocat_vsop87_calc_series(jupiter_longitude_l3, LONG_L3, t);
	L4 = indigocat_vsop87_calc_series(jupiter_longitude_l4, LONG_L4, t);
	L5 = indigocat_vsop87_calc_series(jupiter_longitude_l5, LONG_L5, t);
	position->L = (L0 + L1 * t + L2 * t2 + L3 * t3 + L4 * t4 + L5 * t5);

	/* calc B series */
	B0 = indigocat_vsop87_calc_series(jupiter_latitude_b0, LAT_B0, t);
	B1 = indigocat_vsop87_calc_series(jupiter_latitude_b1, LAT_B1, t);
	B2 = indigocat_vsop87_calc_series(jupiter_latitude_b2, LAT_B2, t);
	B3 = indigocat_vsop87_calc_series(jupiter_latitude_b3, LAT_B3, t);
	B4 = indigocat_vsop87_calc_series(jupiter_latitude_b4, LAT_B4, t);
	B5 = indigocat_vsop87_calc_series(jupiter_latitude_b5, LAT_B5, t);
	position->B = (B0 + B1 * t + B2 * t2 + B3 * t3 + B4 * t4 + B5 * t5);

	/* calc R series */
	R0 = indigocat_vsop87_calc_series(jupiter_radius_r0, RADIUS_R0, t);
	R1 = indigocat_vsop87_calc_series(jupiter_radius_r1, RADIUS_R1, t);
	R2 = indigocat_vsop87_calc_series(jupiter_radius_r2, RADIUS_R2, t);
	R3 = indigocat_vsop87_calc_series(jupiter_radius_r3, RADIUS_R3, t);
	R4 = indigocat_vsop87_calc_series(jupiter_radius_r4, RADIUS_R4, t);
	R5 = indigocat_vsop87_calc_series(jupiter_radius_r5, RADIUS_R5, t);
	position->R = (R0 + R1 * t + R2 * t2 + R3 * t3 + R4 * t4 + R5 * t5);

	position->L = RAD2DEG * position->L;
	position->B = RAD2DEG * position->B;
	position->L = indigocat_range_degrees(position->L);

	indigocat_vsop87_to_fk5(position, JD);
}
